// Current language
let currentLang = 'en';

// Initialize the page
document.addEventListener('DOMContentLoaded', function() {
    // Set up language buttons
    setupLanguageButtons();
    
    // Load gallery
    loadGallery();
    
    // Set initial language
    setLanguage(currentLang);
    
    // Smooth scrolling for navigation links
    setupSmoothScrolling();
});

// Set up language buttons
function setupLanguageButtons() {
    const langButtons = document.querySelectorAll('.lang-btn');
    
    langButtons.forEach(button => {
        button.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            
            // Update active button
            langButtons.forEach(btn => btn.classList.remove('active'));
            this.classList.add('active');
            
            // Change language
            setLanguage(lang);
        });
    });
}

// Set language
function setLanguage(lang) {
    currentLang = lang;
    
    // Update all elements with data-key attribute
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update gallery captions
    updateGalleryCaptions(lang);
    
    // Save language preference to localStorage
    localStorage.setItem('sense_language', lang);
}

// Load gallery images
function loadGallery() {
    const galleryGrid = document.querySelector('.gallery-grid');
    
    if (!galleryGrid) return;
    
    galleryImages.forEach((image, index) => {
        const galleryItem = document.createElement('div');
        galleryItem.className = 'gallery-item';
        
        galleryItem.innerHTML = `
            <img src="${image.src}" alt="${image.alt}" loading="lazy">
            <div class="gallery-caption" data-key="gallery_room${index + 1}">
                ${translations[currentLang][`gallery_room${index + 1}`] || image.alt}
            </div>
        `;
        
        galleryGrid.appendChild(galleryItem);
    });
}

// Update gallery captions when language changes
function updateGalleryCaptions(lang) {
    const captions = document.querySelectorAll('.gallery-caption');
    
    captions.forEach((caption, index) => {
        const key = `gallery_room${index + 1}`;
        if (translations[lang] && translations[lang][key]) {
            caption.textContent = translations[lang][key];
        }
    });
}

// Smooth scrolling for navigation links
function setupSmoothScrolling() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 80,
                    behavior: 'smooth'
                });
            }
        });
    });
}

// Check for saved language preference
function checkSavedLanguage() {
    const savedLang = localStorage.getItem('sense_language');
    if (savedLang && translations[savedLang]) {
        currentLang = savedLang;
        
        // Update active button
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === savedLang) {
                btn.classList.add('active');
            }
        });
        
        setLanguage(savedLang);
    }
}

